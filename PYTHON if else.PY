x=int(input("ENTER ANY NUMBER "))
if x>0:
    print("THE NUMBER IS POSITIVE (+)")
elif x<0:
    print("THE NUMBER IS NEGATIVE (-)")
else:
    print("THE NUMBER IS 0 ") 